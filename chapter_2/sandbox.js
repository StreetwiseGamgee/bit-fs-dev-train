// let allows us to create, and define a value for a variable.
let age = 25;
let year = 2019;

// Prints result to console.
console.log(age, year); // The comma allows for multiple values to be retrieved.

age = 30; // Defining a new value for an existing varible.
console.log(age); // When invoked again, it will display the updated value declared.

// Defines an un-updatible variable, unlike above where age had the abiity to change.
const points = 100;
console.log(points);

var score = 75; // Legacy means of declaring a variable.
console.log(score);